// SumAllTails, where it will calculate the totals of the "tails" of each slice. 
// The tail of a collection is all items in the collection except the first one (the "head").
func TestSumAllTails(t *testing.T) {


	// We could've created a new function checkSums like we normally do, but in this case, 
	// we're showing a new technique, assigning a function to a variable. It might look strange but, 
	// it's no different to assigning a variable to a string, or an int, functions in effect are values too.
	
	checkSums := func(t testing.TB, got, want []int) {
		t.Helper()
		if !reflect.DeepEqual(got, want) {
			t.Errorf("got %v want %v", got, want)
		}
	}

	t.Run("make the sums of tails of", func(t *testing.T) {
		got := SumAllTails([]int{1, 2}, []int{0, 9})
		want := []int{2, 9}
		checkSums(t, got, want)
	})

	t.Run("safely sum empty slices", func(t *testing.T) {
		got := SumAllTails([]int{}, []int{3, 4, 5})
		want := []int{0, 9}
		checkSums(t, got, want)
	})

}